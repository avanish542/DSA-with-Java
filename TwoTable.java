import java.util.*;
public class TwoTable {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for(int i = 1; i<= 10; i++){
            System.out.println("Table of any n Number is :" + i*n);
        };
        System.out.println();
    }
}